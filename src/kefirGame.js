class KefirGame extends TextGame {
    constructor() {
        super(kefirScenes);
        this.currentScene = 'start';
        
        // –ó–∞–≥—Ä—É–∂–∞–µ–º —Å–æ—Ö—Ä–∞–Ω–µ–Ω–Ω–æ–µ —Å–æ—Å—Ç–æ—è–Ω–∏–µ
        loadGameState();
    }
    
    // –ü–µ—Ä–µ–æ–ø—Ä–µ–¥–µ–ª—è–µ–º –º–µ—Ç–æ–¥ –∑–∞–≥—Ä—É–∑–∫–∏ —Å—Ü–µ–Ω—ã
    loadScene(sceneId) {
        const scene = this.scenes[sceneId];
        if (!scene) return;
        
        this.currentScene = sceneId;
        
        // –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–∞ onLoad
        if (scene.onLoad) {
            scene.onLoad();
        }
        
        // –ü–æ–ª—É—á–∞–µ–º —Ç–µ–∫—Å—Ç —Å—Ü–µ–Ω—ã
        let processedText = scene.text;
        
        // –ü–æ–ª—É—á–∞–µ–º –≤–∞—Ä–∏–∞–Ω—Ç—ã –≤—ã–±–æ—Ä–∞ —Å —É—á–µ—Ç–æ–º –¥–∏–Ω–∞–º–∏—á–µ—Å–∫–æ–≥–æ –º–µ—Ç–æ–¥–∞ getChoices
        const choices = scene.getChoices ? scene.getChoices() : scene.choices;
        
        // –û—Ç–æ–±—Ä–∞–∂–∞–µ–º –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å
        this.storyTextElement.innerHTML = processedText;
        this.renderKefirChoices(choices);
        
        // –û–±–Ω–æ–≤–ª—è–µ–º –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏–µ –Ω–∞–π–¥–µ–Ω–Ω—ã—Ö —É–ª–∏–∫
        this.renderClues();
    }
    
    // –ú–µ—Ç–æ–¥ –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è –≤—ã–±–æ—Ä–æ–≤
    renderKefirChoices(choices) {
        this.choicesElement.innerHTML = '';
        
        if (!choices || choices.length === 0) return;
        
        choices.forEach(choice => {
            const choiceButton = document.createElement('button');
            choiceButton.className = 'choice';
            choiceButton.textContent = choice.text;
            
            choiceButton.addEventListener('click', () => {
                this.loadScene(choice.nextScene);
            });
            
            this.choicesElement.appendChild(choiceButton);
        });
    }
    
    // –ú–µ—Ç–æ–¥ –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è –Ω–∞–π–¥–µ–Ω–Ω—ã—Ö —É–ª–∏–∫ –∏ —Å–æ—Å—Ç–æ—è–Ω–∏—è
    renderClues() {
        this.inventoryElement.innerHTML = '';
        
        // –°–æ–∑–¥–∞–µ–º —Å–ø–∏—Å–æ–∫ —É–ª–∏–∫
        const clueItems = [];
        if (gameState.foundClues.catHair) clueItems.push('–ö–æ—à–∞—á—å–∏ –≤–æ–ª–æ—Å—ã –Ω–∞ –∫–Ω–∏–∂–Ω–æ–π –ø–æ–ª–∫–µ');
        if (gameState.foundClues.pawPrints) clueItems.push('–°–ª–µ–¥—ã –ª–∞–ø –Ω–∞ –ø–æ–¥–æ–∫–æ–Ω–Ω–∏–∫–µ');
        if (gameState.foundClues.chewedCase) clueItems.push('–ü–æ–≥—Ä—ã–∑–µ–Ω–Ω—ã–π —Ñ—É—Ç–ª—è—Ä –æ—Ç –æ—á–∫–æ–≤');
        if (gameState.foundClues.scratchedCurtains) clueItems.push('–ó–∞—Ü–µ–ø–∫–∏ –Ω–∞ —à—Ç–æ—Ä–∞—Ö');
        if (gameState.foundClues.movedVase) clueItems.push('–°–¥–≤–∏–Ω—É—Ç–∞—è –≤–∞–∑–∞');
        
        if (clueItems.length === 0) {
            this.inventoryElement.textContent = '–ü–æ–∫–∞ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ –Ω–∏ –æ–¥–Ω–æ–π —É–ª–∏–∫–∏.';
            return;
        }
        
        // –°–æ–∑–¥–∞–µ–º —ç–ª–µ–º–µ–Ω—Ç—ã –¥–ª—è –∫–∞–∂–¥–æ–π —É–ª–∏–∫–∏
        clueItems.forEach(clue => {
            const clueElement = document.createElement('div');
            clueElement.className = 'clue-item';
            clueElement.textContent = clue;
            this.inventoryElement.appendChild(clueElement);
        });
        
        // –î–æ–±–∞–≤–ª—è–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –≤—Ä–µ–º–µ–Ω–∏ –∏ –≥–æ–ª–æ–¥–µ
        const statusElement = document.createElement('div');
        statusElement.style.marginTop = '10px';
        statusElement.innerHTML = `–í—Ä–µ–º—è: ${8 + Math.floor(gameState.timeElapsed / 4)}:${(gameState.timeElapsed % 4) * 15} | `;
        
        // –û—Ç–æ–±—Ä–∞–∂–∞–µ–º —É—Ä–æ–≤–µ–Ω—å –≥–æ–ª–æ–¥–∞
        let hungerText = '–ì–æ–ª–æ–¥: ';
        for (let i = 0; i < gameState.hunger; i++) {
            hungerText += 'üçΩÔ∏è';
        }
        for (let i = gameState.hunger; i < 5; i++) {
            hungerText += '‚ö™';
        }
        
        statusElement.innerHTML += hungerText;
        
        this.inventoryElement.appendChild(statusElement);
        
        // –ï—Å–ª–∏ –µ—Å—Ç—å —Ç–µ–ª–µ—Ñ–æ–Ω, –ø–æ–∫–∞–∑—ã–≤–∞–µ–º —ç—Ç–æ
        if (gameState.hasFoundPhone) {
            const phoneElement = document.createElement('div');
            phoneElement.className = 'clue-item';
            phoneElement.textContent = '–£ –≤–∞—Å –µ—Å—Ç—å —Ç–µ–ª–µ—Ñ–æ–Ω';
            phoneElement.style.backgroundColor = '#e8f4ff';
            this.inventoryElement.appendChild(phoneElement);
        }
    }
}

